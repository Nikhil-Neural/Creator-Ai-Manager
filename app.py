import requests
import json

def fetch_live_trends(niche_topic):
    """
    Serper API se direct live YouTube video links aur titles nikalna.
    Core API integration strictly preserved to feed clean real-time data to CrewAI.
    """
    if not SERPER_KEY:
        return []
    
    url = "https://google.serper.dev/search"
    payload = json.dumps({
        "q": f"site:youtube.com watch viral video {niche_topic}",
        "num": 5
    })
    headers = {
        'X-API-KEY': SERPER_KEY,
        'Content-Type': 'application/json'
    }
    
    video_trends = []
    try:
        response = requests.post(url, headers=headers, data=payload, timeout=10)
        data = response.json()
        
        if "organic" in data:
            for item in data["organic"]:
                link = item.get("link", "")
                if "youtube.com/watch" in link or "youtu.be" in link:
                    video_trends.append({
                        "title": item.get("title", "Trending Video Blueprint"),
                        "url": link
                    })
                if len(video_trends) >= 3:
                    break
        return video_trends
    except Exception as e:
        print(f"[RADAR ERROR] Blueprint link extraction failed: {str(e)}")
        return []

